public class Quotient_remainder {

	public static void main (String[] args)
	{
		int no1 = 11; 
		int no2 = 12;
		int quotient=no1 / no2;
		int remainder=no1 % no2;
		System.out.println("Quotient is:"+quotient);
		System.out.println("Remainder is:"+remainder);
	}
}
